            <span class="temp">{{ weatherInfo.temperature }}‚ÑÉ</span>
            <span class="weather">{{ weatherInfo.weather }}</span>
          </div>
          <div class="weather-detail">
            <span>{{ weatherInfo.windDirection }}È£é {{ weatherInfo.windPower }}Á∫ß</span>
            <span>ÊπøÂ∫¶ {{ weatherInfo.humidity }}%</span>
          </div>
          <div class="report-time">{{ weatherInfo.reportTime }}</div>
        </div>

        <div class="search-box">
          <input 
            type="text" 
            id="keyword" 
            v-model="keyword"
            placeholder="ËæìÂÖ•ÂÖ≥ÈîÆÂ≠óÊêúÁ¥¢"
            @keyup.enter="handleSearch"
          />
        </div>
        <div class="result-panel scrollbar1">
          <!-- ÂàÜÈ°µÂô® -->
          <ul class="pagination-sm">
            <li v-if="currentPage > 1" @click="goPage(1)">È¶ñÈ°µ</li>
            <li v-if="currentPage < totalPages" @click="goPage(currentPage + 1)">‰∏ã‰∏ÄÈ°µ</li>
          </ul>
          <!-- ÁªìÊûúÂàóË°® -->
          <ul class="poi-list">
            <li v-for="(item, index) in searchResults" 
                :key="item.id"
                class="poibox"
                :class="{ selected: selectedId === item.id }"
                @click="jumpToMarker(index)"
                @mouseenter="handlePoiHover(item, index, true)"
                @mouseleave="handlePoiHover(item, index, false)">
              <div v-if="item.photos && item.photos[0]" class="poi-imgbox">
                <span class="poi-img" :style="{ backgroundImage: `url(${item.photos[0].url})` }"></span>
              </div>
              <div class="poi-info-left">
                <h3 class="poi-title">{{ index + 1 }}. {{ item.name }}</h3>
                <div class="poi-info">
                  <p class="poi-addr">Âú∞ÂùÄÔºö{{ item.address }}</p>
                  <p v-if="item.tel" class="poi-addr">ÁîµËØùÔºö{{ item.tel }}</p>
                </div>
              </div>
            </li>
