<script setup>
import { ref, onMounted, nextTick } from 'vue';

const keyword = ref('');
const weatherInfo = ref(null);
let map = null;
let autocomplete = null;
let placeSearch = null;
let weather = null;
const isExpanded = ref(false);
const searchResults = ref([]);
const currentPage = ref(1);
const totalPages = ref(0);
const selectedId = ref(null);
const markers = ref([]);

// Âä†ËΩΩÂÆâÂÖ®ÈÖçÁΩÆËÑöÊú¨
const loadSecurityScript = () => {
  return new Promise((resolve, reject) => {
    const script = document.createElement('script');
    script.src = '//g.alicdn.com/AWSC/AWSC/awsc.js';
    script.async = true;
    script.onload = resolve;
    script.onerror = reject;
    document.head.appendChild(script);
  });
};

// Âä†ËΩΩÈ´òÂæ∑Âú∞ÂõæËÑöÊú¨
const loadAMapScript = () => {
  return new Promise((resolve, reject) => {
    // Âà†Èô§ÂèØËÉΩÂ≠òÂú®ÁöÑÊóßÂÆû‰æã
    delete window.AMap;
    
    // ËÆæÁΩÆÂÆâÂÖ®ÈÖçÁΩÆ
    window._AMapSecurityConfig = {
      securityJsCode: import.meta.env.VITE_AMAP_SECRET, // ‰ΩøÁî®ÁéØÂ¢ÉÂèòÈáè‰∏≠ÁöÑÂÆâÂÖ®ÂØÜÈí•
      serviceHost: '/_AMapService', // ‰ª£ÁêÜÊúçÂä°Ë∑ØÁî±
    }

    const script = document.createElement('script');
    script.src = `https://webapi.amap.com/maps?v=1.4.15&key=${import.meta.env.VITE_AMAP_KEY}`;
    script.type = 'text/javascript';
    script.async = true;
    script.onload = resolve;
    script.onerror = reject;
    document.head.appendChild(script);
  });
};

// Êü•ËØ¢Â§©Ê∞î
const queryWeather = async (city) => {
  return new Promise((resolve, reject) => {
    if (!weather) {
      reject(new Error('Â§©Ê∞îÊèí‰ª∂Êú™ÂàùÂßãÂåñ'));
      return;
    }
    
    weather.getLive(city, (err, data) => {
      if (err) {
        reject(err);
        return;
      }
      resolve(data);
    });
  });
};

// ÂàùÂßãÂåñÂú∞ÂõæÂíåÊêúÁ¥¢
const initMap = () => {
  if (!document.getElementById('mapContainer')) {
    console.error('Âú∞ÂõæÂÆπÂô®Êú™ÊâæÂà∞');
    return;
  }

  // ÂàõÂª∫Âú∞ÂõæÂÆû‰æã
  map = new window.AMap.Map('mapContainer', {
    resizeEnable: true,
    rotateEnable: true,
    pitchEnable: true,
    center: [116.397428, 39.90923],
    zoom: 10,
    pitch: 0,
    rotation: 0,
    viewMode: '3D',
    buildingAnimation: true,
    expandZoomRange: true,
    zooms: [3, 20],
    keyboardEnable: false
  });

  console.log('Âú∞ÂõæÂÆû‰æãÂàõÂª∫ÊàêÂäü:', map);

  // Âä†ËΩΩÊèí‰ª∂
  window.AMap.plugin([
    'AMap.Autocomplete',
    'AMap.PlaceSearch',
    'AMap.Weather',
    'AMap.ControlBar'
  ], () => {
    // Ê∑ªÂä† 3D ÊéßÂà∂Êéß‰ª∂
    map.addControl(new window.AMap.ControlBar({
      showZoomBar: false,
      showControlButton: true,
      position: {
        right: '10px',
        top: '80px'  // ÈÅøÂÖç‰∏éÊêúÁ¥¢Ê°ÜÈáçÂè†
      }
    }));

    // ÂàùÂßãÂåñËá™Âä®ÂÆåÊàê
    const autoOptions = {
      city: 'ÂÖ®ÂõΩ',
      input: 'keyword'
    };
    autocomplete = new window.AMap.Autocomplete(autoOptions);

    // ÂàùÂßãÂåñÂú∞ÁÇπÊêúÁ¥¢
    placeSearch = new window.AMap.PlaceSearch({
      city: 'ÂÖ®ÂõΩ',
      map: map,
      pageSize: 10,
      autoFitView: true
    });

    // ÂàùÂßãÂåñÂ§©Ê∞îÊü•ËØ¢
    weather = new window.AMap.Weather();

    // Ê≥®ÂÜåÈÄâÊã©‰∫ã‰ª∂
    window.AMap.event.addListener(autocomplete, 'select', async (e) => {
      console.log('ÈÄâ‰∏≠POI:', e.poi);
      const cityName = e.poi.adcode ? e.poi.adcode : e.poi.name;
      
      // ÊêúÁ¥¢‰ΩçÁΩÆ
      placeSearch.setCity(e.poi.adcode);
      placeSearch.search(e.poi.name);

      // Êü•ËØ¢Â§©Ê∞î
      try {
        const weatherData = await queryWeather(cityName);
        weatherInfo.value = weatherData;
        console.log('Â§©Ê∞î‰ø°ÊÅØ:', weatherData);
      } catch (error) {
        console.error('Ëé∑ÂèñÂ§©Ê∞î‰ø°ÊÅØÂ§±Ë¥•:', error);
      }
    });
  });
};

const toggleMap = async () => {
  isExpanded.value = !isExpanded.value;
  
  if (isExpanded.value) {
    // Á≠âÂæÖ DOM Êõ¥Êñ∞ÂÆåÊàê
    await nextTick();
    
    // Â¶ÇÊûúÂú∞ÂõæÊú™ÂàùÂßãÂåñÔºåÂàôÂàùÂßãÂåñ
    if (!map) {
      try {
        initMap();
        console.log('Âú∞ÂõæÂàùÂßãÂåñÂÆåÊàê');
      } catch (error) {
        console.error('Âú∞ÂõæÂàùÂßãÂåñÂ§±Ë¥•:', error);
      }
    } else {
      // Â¶ÇÊûúÂú∞ÂõæÂ∑≤ÁªèÂàùÂßãÂåñÔºåÂà∑Êñ∞‰∏Ä‰∏ãÂ§ßÂ∞è
      map.resize();
    }
  }
};

onMounted(async () => {
  try {
    // ÂÖàÂä†ËΩΩËÑöÊú¨Ôºå‰ΩÜ‰∏çÂàùÂßãÂåñÂú∞Âõæ
    await loadSecurityScript();
    await loadAMapScript();
    console.log('Âú∞ÂõæËÑöÊú¨Âä†ËΩΩÂÆåÊàê');
  } catch (error) {
    console.error('Âä†ËΩΩÂú∞ÂõæËÑöÊú¨Â§±Ë¥•:', error);
  }
});

// Â§ÑÁêÜÊêúÁ¥¢
const handleSearch = () => {
  if (!keyword.value) return;
  currentPage.value = 1;
  goPage(1);
};

// Ë∑≥ËΩ¨Âà∞ÊåáÂÆöÈ°µ
const goPage = (page) => {
  if (!placeSearch) return;
  
  placeSearch.setPageIndex(page);
  placeSearch.search(keyword.value, async (status, result) => {
    if (status === 'complete') {
      searchResults.value = result.poiList.pois;
      totalPages.value = Math.ceil(result.poiList.count / result.poiList.pageSize);
      currentPage.value = page;
      
      // Êõ¥Êñ∞Âú∞ÂõæÊ†áËÆ∞
      updateMarkers(result.poiList.pois);

      // Êü•ËØ¢Á¨¨‰∏Ä‰∏™Âú∞ÁÇπÁöÑÂ§©Ê∞î
      if (result.poiList.pois.length > 0) {
        const firstPoi = result.poiList.pois[0];
        try {
          const weatherData = await queryWeather(firstPoi.cityname || firstPoi.adname);
          weatherInfo.value = weatherData;
        } catch (error) {
          console.error('Ëé∑ÂèñÂ§©Ê∞î‰ø°ÊÅØÂ§±Ë¥•:', error);
        }
      }
    }
  });
};

// Êõ¥Êñ∞Âú∞ÂõæÊ†áËÆ∞
const updateMarkers = (pois) => {
  // Ê∏ÖÈô§ÊóßÊ†áËÆ∞
  markers.value.forEach(marker => marker.remove());
  markers.value = [];
  
  // Ê∑ªÂä†Êñ∞Ê†áËÆ∞
  pois.forEach((poi, index) => {
    const marker = new window.AMap.SimpleMarker({
      map: map,
      position: poi.location,
      iconTheme: 'numv1',
      iconStyle: `red-${index + 1}`,
      containerClassNames: 'my-marker'
    });
    
    markers.value.push(marker);
  });
  
  // Ë∞ÉÊï¥ËßÜÈáé
  map.setFitView(markers.value);
};

// ÈÄâÊã© POI
const selectPoi = (poi, index) => {
  selectedId.value = poi.id;
  
  // Êõ¥Êñ∞Ê†áËÆ∞Ê†∑Âºè
  markers.value[index].setIconStyle(`blue-${index + 1}`);
  
  // Ëé∑ÂèñÊ†áËÆ∞‰ΩçÁΩÆ
  const position = poi.location;
  
  // Âπ≥ÊªëÁßªÂä®Âà∞Ê†áËÆ∞‰ΩçÁΩÆ
  map.value.setZoomAndCenter(15, position, true, {
    duration: 800,
    easing: 'easeInOutCubic'
  });
  
  // ÊâìÂºÄ‰ø°ÊÅØÁ™ó‰Ωì
  const infoWindow = new window.AMap.SimpleInfoWindow({
    offset: new window.AMap.Pixel(0, -32),
    infoTitle: poi.name,
    infoBody: poi.address
  });
  
  infoWindow.open(map.value, position);
};

// Â§ÑÁêÜÊÇ¨ÂÅúÊïàÊûú
const handlePoiHover = (poi, index, isEnter) => {
  if (selectedId.value === poi.id) return;
  
  const marker = markers.value[index];
  if (marker) {
    marker.setIconStyle(isEnter ? `blue-${index + 1}` : `red-${index + 1}`);
    
    // ÊÇ¨ÂÅúÊó∂‰πüÊòæÁ§∫‰ø°ÊÅØÁ™ó‰Ωì
    if (isEnter) {
      const infoWindow = new window.AMap.SimpleInfoWindow({
        offset: new window.AMap.Pixel(0, -32),
        infoTitle: poi.name,
        infoBody: poi.address
      });
      infoWindow.open(map.value, poi.location);
    }
  }
};

// Ê∑ªÂä†Ë∑≥ËΩ¨Âà∞ÊåáÂÆöÊ†áËÆ∞ÁöÑÊñπÊ≥ï
const jumpToMarker = async (index) => {
  if (!map.value || !markers.value[index]) return;
  
  const marker = markers.value[index];
  const position = marker.getPosition();
  const poi = searchResults.value[index];
  
  // Âπ≥ÊªëÁßªÂä®Âà∞Ê†áËÆ∞‰ΩçÁΩÆ
  map.value.setZoomAndCenter(15, position, true, {
    duration: 800,
    easing: 'easeInOutCubic'
  });
  
  // Ëß¶ÂèëÊ†áËÆ∞ÁöÑÁÇπÂáª‰∫ã‰ª∂
  marker.emit('click');

  // Êü•ËØ¢Â§©Ê∞î
  try {
    const weatherData = await queryWeather(poi.cityname || poi.adname);
    weatherInfo.value = weatherData;
  } catch (error) {
    console.error('Ëé∑ÂèñÂ§©Ê∞î‰ø°ÊÅØÂ§±Ë¥•:', error);
  }
};
</script>

